package Day11_17;

public class OperatorDemo03 {
    public static void main(String[] args) {
        int i=10;
        int j=3;
        System.out.println(i+"+"+j+"="+(i+j));
        System.out.println(i+"-"+j+"="+(i-j));
        System.out.println(i+"*"+j+"="+(i*j));
        System.out.println(i+"/"+j+"="+(i/j));
        System.out.println(i+"/"+j+"="+(float)(i/j));
        System.out.println(i+"/"+j+"="+(float)i/j);
        System.out.println(i+"%"+j+"="+(i%j));
    }
}
